package model;

public class EmMember {
	private String emId;
	private String emPassword;
	private String emPhone;
	private String emName;
	private String emBirthdate;
	private int emGender;
	public String getEmId() {
		return emId;
	}
	public void setEmId(String emId) {
		this.emId = emId;
	}
	public String getEmPassword() {
		return emPassword;
	}
	public void setEmPassword(String emPassword) {
		this.emPassword = emPassword;
	}
	public String getEmPhone() {
		return emPhone;
	}
	public void setEmPhone(String emPhone) {
		this.emPhone = emPhone;
	}
	public String getEmName() {
		return emName;
	}
	public void setEmName(String emName) {
		this.emName = emName;
	}
	public String getEmBirthdate() {
		return emBirthdate;
	}
	public void setEmBirthdate(String emBirthdate) {
		this.emBirthdate = emBirthdate;
	}
	public int getEmGender() {
		return emGender;
	}
	public void setEmGender(int emGender) {
		this.emGender = emGender;
	}
	
	
}
